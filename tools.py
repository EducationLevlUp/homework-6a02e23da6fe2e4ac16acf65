# ruff: noqa: I001
from __future__ import annotations

from langchain_core.tools import tool

from chunking import split_text
from vector_store import add_documents, search

# ---------------------------------------------------------------------------
# Инструменты агента для работы с RAG-хранилищем.
#
# Каждый инструмент обернут декоратором @tool и возвращает строку,
# которую агент может использовать в своём ответе.
# ---------------------------------------------------------------------------

# ---------------------------------------------------------------------------
# Инструмент: поиск по базе знаний
# ---------------------------------------------------------------------------

@tool
def search_knowledge_base(query: str, max_results: int = 4) -> str:
    """
    Семантический поиск по базе знаний.

    Параметры
    ----------
    query : str
        Текстовый запрос для поиска.
    max_results : int
        Максимальное количество результатов (по умолчанию 4).

    Возвращает
    ----------
    str
        Форматированный список найденных документов с метрикой релевантности.
    """
    results = search(query=query, k=max_results)

    if not results:
        return "По запросу ничего не найдено в базе знаний."

    lines: list[str] = []
    for doc, score in results:
        title = doc.metadata.get("title", "Без названия")
        lines.append(f"[{title}] (релевантность: {score:.4f})\n{doc.page_content}")

    return "\n\n---\n\n".join(lines)


# ---------------------------------------------------------------------------
# Инструмент: добавление в базу знаний
# ---------------------------------------------------------------------------

@tool
def add_to_knowledge_base(content: str, title: str) -> str:
    """
    Добавляет документ в базу знаний.

    Текст автоматически разбивается на чанки перед сохранением.

    Параметры
    ----------
    content : str
        Содержимое документа.
    title : str
        Заголовок документа.

    Возвращает
    ----------
    str
        Сообщение об успехе с количеством добавленных чанков.
    """
    chunks = split_text(text=content, title=title)
    doc_ids = add_documents(chunks)

    return (
        f"Документ «{title}» успешно добавлен в базу знаний. "
        f"Создано {len(chunks)} чанков, ID: {', '.join(doc_ids[:5])}"
        + ("..." if len(doc_ids) > 5 else "")
    )


# ---------------------------------------------------------------------------
# Список инструментов для передачи агенту
# ---------------------------------------------------------------------------

TOOLS = [search_knowledge_base, add_to_knowledge_base]
